def obs_to_prompt(obs_dict: Dict[str, Any]) -> str:
    """Format a HeistObservation dict into a text prompt."""
    phase   = obs_dict.get("current_phase", "AlertTriage")
    budget  = obs_dict.get("budget_remaining", 50)
    beliefs = obs_dict.get("bayesian_beliefs", {})
    flagged = obs_dict.get("flagged_transaction") or {}
    morph   = obs_dict.get("morph_alert", {})

    # Top-5 suspicious entities (more context for SAR filing)
    top5 = sorted(beliefs.items(), key=lambda x: -x[1])[:5]
    belief_str = " | ".join(f"{e}={p:.2f}" for e, p in top5) or "none"

    flagged_str = (
        f"{flagged.get('source_entity','?')} -> {flagged.get('target_entity','?')}"
        f" ${flagged.get('amount', 0):.0f}"
        if flagged else "none"
    )

    morph_str = f"MORPH_ALERT invalidated={morph.get('invalidated_entities', [])}" if morph.get("morph_occurred") else ""

    # Budget urgency — critical signal for efficiency learning
    if budget <= 10:
        urgency = f"🚨 CRITICAL: Only {budget} budget left! FILE SAR NOW!\n"
    elif budget <= 20:
        urgency = f"⚠️ WARNING: {budget} budget left. File SAR soon to maximize efficiency.\n"
    elif phase == "SARFiling":
        urgency = "✅ SARFiling phase reached. FILE SAR IMMEDIATELY on top suspects.\n"
    else:
        urgency = ""

    return (
        f"[PHASE:{phase}] [BUDGET:{budget}]\n"
        f"{urgency}"
        f"FLAGGED: {flagged_str}\n"
        f"TOP_SUSPECTS: {belief_str}\n"
        f"{morph_str}\n"
        "CHOOSE ACTION:"
    )


def parse_action(text: str, beliefs: Dict[str, float]) -> Tuple[str, Dict[str, Any]]:
    """
    Parse model output into (action_type, params).
    Gracefully falls back to heuristic if parsing fails.
    """
    import re
    text = text.strip()

    # Try structured parse
    action_match = re.search(r"ACTION:(\w+)", text)
    entity_match = re.search(r"ENTITY:(\S+)", text)
    entities_match = re.search(r"ENTITIES:([\w,_\-]+)", text)

    if not action_match:
        # Fallback: first action keyword found in text
        for act in ACTION_VOCAB:
            if act in text:
                action_match_str = act
                break
        else:
            action_match_str = "query_transactions"
    else:
        action_match_str = action_match.group(1)

    # Normalise action name
    action_type = action_match_str.strip()
    if action_type not in ACTION_VOCAB:
        action_type = "query_transactions"

    # Entity
    if action_type == "file_SAR":
        if entities_match:
            entities = entities_match.group(1).split(",")
        else:
            entities = list(beliefs.keys())[:5]
        return action_type, {"evidence_chain": entities}
    else:
        if entity_match:
            entity = entity_match.group(1)
        elif beliefs:
            entity = max(beliefs, key=beliefs.get)
        else:
            entity = ""
        return action_type, {"entity_id": entity}


# ---------------------------------------------------------------------------
# Rollout: run one episode, return trajectory
# ---------------------------------------------------------------------------

def run_rollout(
    env: HeistEnvironment,
    model,
    tokenizer,
    seed: int,
    use_model: bool = True,
    verbose: bool = False,
    expert=None,
    episode_num: int = 0,
    temperature: float = 0.7,
    preferred_scheme_type: Optional[str] = None,
) -> Dict[str, Any]:
    """
    Run one complete episode.

    preferred_scheme_type: if set, passed to env.reset() so the Red Queen
    curriculum drives which scheme the investigator faces this episode.

    Returns a dict with:
        prompts:     List[str]   — text prompt at each step
        completions: List[str]   — model output at each step
        rewards:     List[float] — step reward (0 except last = R_investigator)
        r_inv:       float       — final R_investigator score
        scheme_type: str
        f1_by_type:  Dict[str, float]
    """
    obs = env.reset(seed=seed, scheme_type=preferred_scheme_type)
    obs_dict = obs.model_dump()
    scheme_type = obs_dict.get("metadata", {}).get("scheme_type", "unknown")

    prompts:     List[str]   = []
    completions: List[str]   = []
    step_rewards: List[float] = []
    evidence_chain: List[str] = []
    subpoenaed: set = set()
    action_type_log: List[str] = []   # interpretability: track action distribution

    done = False
    steps = 0
    final_compliance = 0.0
    final_beliefs: Dict[str, float] = {}

    while not done and steps < 50:
        steps += 1
        prompt_text = obs_dict_to_full_prompt(obs_dict)
        beliefs = obs_dict.get("bayesian_beliefs", {})

        if use_model and model is not None:
            # Generate one action token sequence
            inputs = tokenizer(
                prompt_text, return_tensors="pt", truncation=True, max_length=MAX_SEQ_LEN
            )
            import torch
            device = next(model.parameters()).device
            inputs = {k: v.to(device) for k, v in inputs.items()}
            with torch.no_grad():
                outputs = model.generate(
                    **inputs,
                    max_new_tokens=40,
                    do_sample=True,
                    temperature=temperature,
                    pad_token_id=tokenizer.eos_token_id,
                )
            generated = tokenizer.decode(
                outputs[0][inputs["input_ids"].shape[1]:], skip_special_tokens=True
            )
        else:
            # Heuristic fallback (for testing without GPU)
            generated = _heuristic_action_text(obs_dict)

        action_type_str, params = parse_action(generated, beliefs)

        # For SAR filing: submit only high-confidence entities (P > 0.5) to reduce
        # false positives that tank precision. Previously we submitted ALL queried
        # entities, which capped F1 at ~0.5 because precision was always diluted.
        # Fall back to top-5 by belief if no entity clears the threshold.
        if action_type_str == "file_SAR" and evidence_chain:
            high_conf = [e for e in evidence_chain if beliefs.get(e, 0.0) > 0.5]
            if high_conf:
                params["evidence_chain"] = high_conf
            else:
                top5 = sorted(evidence_chain, key=lambda e: beliefs.get(e, 0.0), reverse=True)[:5]
                params["evidence_chain"] = top5

        # Accumulate evidence
        entity = params.get("entity_id", "")
        if entity and entity not in evidence_chain:
            evidence_chain.append(entity)

        # Step
        action = InvestigatorAction(
            action_type=_ACTION_MAP.get(action_type_str, ActionType.QUERY_TRANSACTIONS),
            params=params,
        )
        obs = env.step(action)
        obs_dict = obs.model_dump()

        # Immediate step reward
        step_r = float(obs_dict.get("reward", 0.0))

        prompts.append(prompt_text)
        completions.append(generated)
        step_rewards.append(step_r)
        action_type_log.append(action_type_str)   # track for entropy

        done = bool(obs_dict.get("done", False))
        final_beliefs = obs_dict.get("bayesian_beliefs", {})

        # Compliance score from SAR
        tool_result = obs_dict.get("tool_result") or {}
        data = tool_result.get("data", {})
        if "compliance_score" in data:
            tool_compliance = float(data["compliance_score"])
            if expert is not None and evidence_chain:
                ev_dicts = [{"entity_id": e} for e in evidence_chain]
                risk_score = max(final_beliefs.values()) * 10.0 if final_beliefs else 5.0
                exp_score, _ = expert.evaluate_SAR(
                    evidence_chain=ev_dicts,
                    risk_score=risk_score,
                    narrative=completions[-1] if completions else "",
                    episode_number=episode_num,
                    query_count=steps,
                )
                # 60% F1-based tool compliance, 40% expert satisfaction (preference drift)
                final_compliance = 0.6 * tool_compliance + 0.4 * exp_score
            else:
                final_compliance = tool_compliance

        if verbose:
            print(f"  step={steps:2d} | {action_type_str:30s} | R={step_r:+.2f} | budget={obs_dict.get('budget_remaining')}")

    gt = env._graph.ground_truth.get(env.state.scheme_id, {})
    true_path  = list(gt.get("full_path", []))
    scheme_t   = gt.get("scheme_type", scheme_type)

    r_inv_result = r_investigator(
        evidence_chain=evidence_chain,
        ground_truth_path=true_path,
        compliance_score=final_compliance,
        queries_used=steps,
        total_budget=50,
        query_history=env.state.query_history,
        graph=env._graph,
        scheme_type=scheme_t,
        is_codex_generated=False,
    )
    r_inv = float(r_inv_result["total"])

    # Assign terminal reward to last step only (sparse)
    if step_rewards:
        step_rewards[-1] += r_inv

    # Scheme-type F1 (for Red Queen curriculum CSV)
    predicted_set = set(evidence_chain)
    true_set = set(true_path)
    if true_set:
        tp = len(predicted_set & true_set)
        precision = tp / max(len(predicted_set), 1)
        recall    = tp / max(len(true_set), 1)
        f1 = 2 * precision * recall / max(precision + recall, 1e-9)
    else:
        f1 = 0.0

    # ── Interpretability metrics ───────────────────────────────────────
    # Action entropy: H = -Σ p(a) log p(a). Low entropy = more decisive.
    # Tracks whether the model is learning a coherent strategy or acting randomly.
    action_counts: Dict[str, int] = {}
    for a in action_type_log:
        action_counts[a] = action_counts.get(a, 0) + 1
    if action_counts:
        total_acts = len(action_type_log)
        import math as _math
        action_entropy = -sum(
            (c / total_acts) * _math.log(c / total_acts + 1e-9)
            for c in action_counts.values()
        )
    else:
        action_entropy = 0.0
    first_action = action_type_log[0] if action_type_log else "none"

    return {
        "prompts":        prompts,
        "completions":    completions,
        "rewards":        step_rewards,
        "r_inv":          r_inv,
        "scheme_type":    scheme_type,
        "f1":             f1,
        "f1_by_type":     {scheme_type: f1},
        "steps":          steps,
        "compliance":     final_compliance,
        "action_entropy": round(action_entropy, 4),
        "first_action":   first_action,
        "action_counts":  action_counts,
    }


def obs_dict_to_full_prompt(obs_dict: Dict[str, Any]) -> str:
    """Full prompt with system + observation."""
    return f"{SYSTEM_PROMPT}\n\n{obs_to_prompt(obs_dict)}"


def _heuristic_action_text(obs_dict: Dict[str, Any]) -> str:
    """Return a heuristic action in model output format (no GPU needed)."""
    phase   = obs_dict.get("current_phase", "AlertTriage")
    beliefs = obs_dict.get("bayesian_beliefs", {})
    flagged = obs_dict.get("flagged_transaction") or {}

    entity = (
        max(beliefs, key=beliefs.get)
        if beliefs
        else flagged.get("source_entity", "unknown")
    )

    phase_actions = {
        "AlertTriage":    "query_transactions",
        "Investigation":  "trace_network",
        "CrossReference": "cross_reference_jurisdiction",
        "SARFiling":      "file_SAR",
    }
    act = phase_actions.get(phase, "query_transactions")

    if act == "file_SAR":
        entities = ",".join(list(beliefs.keys())[:5])
        return f"ACTION:file_SAR ENTITIES:{entities}"
    return f"ACTION:{act} ENTITY:{entity}"


# ---------------------------------------------------------------------------
# Multi-step training helper
# ---------------------------------------------------------------------------

def _sample_key_steps(rollout: Dict, n_train: int = 5) -> List[int]:
    """
    Select n_train step indices per rollout for GRPO gradient updates.

    Always includes step 0 (first decision) and step -1 (final decision).
    Intermediate steps are evenly spaced to cover all phases.

    Training on multiple steps per rollout is critical: the original single
    last-step approach gives only 4 gradient signals per episode (one per G
    rollout). Multi-step gives ~20 signals, covering early phase decisions,
    mid-investigation choices, and the critical SAR-filing moment.
    """
    length = len(rollout["prompts"])
    if length <= n_train:
        return list(range(length))
    indices: List[int] = [0]
    for k in range(1, n_train - 1):
        idx = int(round(k * (length - 1) / (n_train - 1)))
        indices.append(idx)
    indices.append(length - 1)
    return sorted(set(indices))


# ---------------------------------------------------------------------------
# GRPO loss (core math — operates without TRL when testing)
# ---------------------------------------------------------------------------

def grpo_loss(
    model,
    tokenizer,
    prompts: List[str],
    completions: List[str],
    rewards: List[float],
) -> "torch.Tensor":
    """
    GRPO update step.

    For each (prompt, completion) pair:
        advantage_i = (reward_i - mean(rewards)) / (std(rewards) + eps)
        loss = -mean(advantage_i * log_prob(completion | prompt))

    Why GRPO: PPO requires value function unstable on sparse 50-step episodes.
    DPO needs curated pairs unavailable for novel adversarial schemes.
    GRPO operates from reward signal alone.
    """
    import torch

    device = next(model.parameters()).device
    rewards_t = torch.tensor(rewards, dtype=torch.float32, device=device)

    # Normalize rewards within the group (GRPO key operation)
    mean_r = rewards_t.mean()
    std_r  = rewards_t.std() + 1e-8
    advantages = (rewards_t - mean_r) / std_r

    total_loss = torch.tensor(0.0, device=device, requires_grad=True)
    valid_count = 0

    for i, (prompt, completion, adv) in enumerate(zip(prompts, completions, advantages)):
        full_text = prompt + completion
        inputs = tokenizer(
            full_text, return_tensors="pt", truncation=True, max_length=MAX_SEQ_LEN
        ).to(device)

        prompt_len = tokenizer(
            prompt, return_tensors="pt", truncation=True, max_length=MAX_SEQ_LEN
        )["input_ids"].shape[1]

        outputs = model(**inputs, labels=inputs["input_ids"])

        # Only count loss on completion tokens (not prompt)
        shift_logits  = outputs.logits[:, prompt_len-1:-1, :]
        shift_labels  = inputs["input_ids"][:, prompt_len:]

        # Skip if completion was truncated away entirely
        if shift_labels.shape[1] == 0:
            continue

        log_probs = torch.nn.functional.log_softmax(shift_logits, dim=-1)
        token_log_probs = log_probs.gather(
            2, shift_labels.unsqueeze(-1)
        ).squeeze(-1)

        # Mean log prob of completion
        mean_log_prob = token_log_probs.mean()

        # Skip NaN/Inf from extreme logits — don't let one bad step corrupt the batch
        if torch.isnan(mean_log_prob) or torch.isinf(mean_log_prob):
            continue

        # GRPO: maximize advantage-weighted log probability
        step_loss = -adv * mean_log_prob
        total_loss = total_loss + step_loss
        valid_count += 1

    # All steps were invalid — signal caller to skip update
    if valid_count == 0:
        return torch.tensor(float("nan"), device=device)

    return total_loss / valid_count


