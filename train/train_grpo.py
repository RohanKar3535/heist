"""
HEIST GRPO Training Script — Step 11.

Why GRPO?
---------
PPO requires a value function (critic) that is unstable on sparse 50-step episodes
where reward only arrives when the SAR is finally filed. DPO needs curated
preference pairs which do not exist for novel adversarial schemes generated by the
Criminal Codex at runtime. GRPO operates from the reward signal alone: it samples
G rollouts per prompt, normalises them within the group, and updates the policy
without a critic — exactly what a sparse multi-step investigator reward demands.

Architecture
------------
Observation  →  prompt text  →  Qwen2.5-1.5B (4-bit, Unsloth)  →  action token
Action token →  HeistEnvironment.step()  →  next observation + reward
Episode done →  R_investigator computed  →  GRPO loss  →  gradient update

Files written
-------------
checkpoints/ckpt_ep{N}/     — model + tokenizer every 10 episodes
training_curves.json        — reward curve (episode -> R_investigator)
training_log.csv            — per-episode per-scheme-type F1 (for Red Queen)
"""

