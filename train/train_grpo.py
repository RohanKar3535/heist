def _init_csv(path: str) -> None:
    os.makedirs(os.path.dirname(path) if os.path.dirname(path) else ".", exist_ok=True)
    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["episode", "scheme_type", "f1", "r_inv", "steps", "compliance"])


def _append_csv(path: str, episode: int, result: Dict) -> None:
    with open(path, "a", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow([
            episode,
            result.get("scheme_type", ""),
            f"{result.get('f1', 0):.4f}",
            f"{result.get('r_inv', 0):.4f}",
            result.get("steps", 0),
            f"{result.get('compliance', 0):.4f}",
        ])


def _save_curves(path: str, curves: Dict) -> None:
    with open(path, "w", encoding="utf-8") as f:
        json.dump(curves, f, indent=2)


def _save_checkpoint(model, tokenizer, episode: int) -> None:
    ckpt_dir = os.path.join(OUT_DIR, f"ckpt_ep{episode:04d}")
    os.makedirs(ckpt_dir, exist_ok=True)
    model.save_pretrained(ckpt_dir)
    tokenizer.save_pretrained(ckpt_dir)
    print(f"  [Checkpoint] Saved to {ckpt_dir}")


# ---------------------------------------------------------------------------
# Main training loop
# ---------------------------------------------------------------------------

def train(
    num_episodes: int = NUM_EPISODES,
    use_trl_trainer: bool = True,   # False = lightweight manual loop (for CI)
    use_model: bool = True,          # False = heuristic rollouts (for CI without GPU)
    verbose: bool = True,
):
    """
    Full GRPO training loop.

    use_trl_trainer=True  → uses TRL GRPOConfig + standard update path
    use_trl_trainer=False → uses our manual grpo_loss() (same math, no TRL dep)
    use_model=False       → heuristic rollouts (testing/CI, no GPU required)
    """
    print("=" * 70)
    print("HEIST GRPO Training — Qwen2.5-1.5B (4-bit Unsloth)")
    print("=" * 70)
    print(f"  Episodes:      {num_episodes}")
    print(f"  GRPO G:        {GRPO_G} rollouts/prompt")
    print(f"  LR:            {LR}")
    print(f"  Codex update:  every {CODEX_UPDATE_K} episodes")
    print(f"  Checkpoint:    every {CKPT_EVERY} episodes -> {OUT_DIR}")
    print()

    # ── Load model ─────────────────────────────────────────────────────
    model = tokenizer = optimizer = None

    if use_model:
        FastLanguageModel, torch, AutoTokenizer = _import_ml()

        print("[1] Loading Qwen2.5-1.5B with Unsloth 4-bit quantization...")
        model, tokenizer = FastLanguageModel.from_pretrained(
            model_name=MODEL_NAME,
            max_seq_length=MAX_SEQ_LEN,
            load_in_4bit=True,  # 4-bit for T4 (fits in 16GB VRAM)
            dtype=None,         # auto-detect bfloat16/float16
        )

        # LoRA adapters (PEFT — only fine-tune adapter layers)
        model = FastLanguageModel.get_peft_model(
            model,
            r=16,                       # LoRA rank
            target_modules=["q_proj", "k_proj", "v_proj", "o_proj",
                             "gate_proj", "up_proj", "down_proj"],
            lora_alpha=16,
            lora_dropout=0,
            bias="none",
            use_gradient_checkpointing="unsloth",  # saves VRAM on T4
            random_state=42,
        )

        model.train()

        import torch
        optimizer = torch.optim.AdamW(
            [p for p in model.parameters() if p.requires_grad],
            lr=LR,
        )
        print(f"  Model loaded. Trainable params: {sum(p.numel() for p in model.parameters() if p.requires_grad):,}")
    else:
        print("[1] Heuristic mode (no GPU) — model=None")

    # ── Setup env + agents ─────────────────────────────────────────────
    print("[2] Initialising HEIST environment...")
    env = HeistEnvironment()

    # Criminal Designer (optional — skipped if not importable)
    criminal_designer = None
    try:
        from agents.criminal import CriminalDesigner
        criminal_designer = CriminalDesigner(temperature=1.0, seed=42)
        print("  CriminalDesigner loaded.")
    except Exception as e:
        print(f"  CriminalDesigner not available: {e}")

    # ── Init logging ───────────────────────────────────────────────────
    os.makedirs(OUT_DIR, exist_ok=True)
    _init_csv(LOG_CSV)
    curves: Dict[str, Any] = {
        "episodes": [],
        "r_inv":    [],
        "f1":       [],
        "scheme_types": [],
        "config": {
            "model": MODEL_NAME,
            "num_episodes": num_episodes,
            "grpo_g": GRPO_G,
            "lr": LR,
        }
    }

    # ── Training loop ──────────────────────────────────────────────────
    print(f"\n[3] Training for {num_episodes} episodes...\n")
    episode_results: List[Dict] = []

    for ep in range(1, num_episodes + 1):
        t0 = time.time()
        seed = ep * 17  # deterministic but varied seeds

        if verbose:
            print(f"Episode {ep:3d}/{num_episodes}", end="  ")

        # Collect G rollouts (GRPO requires multiple samples per prompt)
        rollouts = []
        for g in range(GRPO_G):
            rollout = run_rollout(
                env=env,
                model=model,
                tokenizer=tokenizer,
                seed=seed + g,
                use_model=use_model,
                verbose=False,
            )
            rollouts.append(rollout)

        # Use best rollout for logging (mean r_inv across group)
        mean_r_inv = float(np.mean([r["r_inv"] for r in rollouts]))
        best_rollout = max(rollouts, key=lambda x: x["r_inv"])

        # ── GRPO update ────────────────────────────────────────────────
        if use_model and model is not None and optimizer is not None:
            # GRPO: normalize across the G rollouts by their terminal R_inv.
            # Each rollout is treated as one group member — advantage computed
            # from final episode reward so intermediate steps aren't penalized.
            import torch
            rollout_rewards = [r["r_inv"] for r in rollouts]
            rewards_t = torch.tensor(rollout_rewards, dtype=torch.float32)
            mean_r = rewards_t.mean()
            std_r  = rewards_t.std() + 1e-8
            advantages = ((rewards_t - mean_r) / std_r).tolist()

            optimizer.zero_grad()
            # One gradient contribution per rollout (last step = SAR decision)
            total_loss = grpo_loss(
                model, tokenizer,
                [r["prompts"][-1]      for r in rollouts],
                [r["completions"][-1]  for r in rollouts],
                advantages,
            )
            total_loss.backward()
            # Gradient clipping (stability on sparse rewards)
            torch.nn.utils.clip_grad_norm_(
                [p for p in model.parameters() if p.requires_grad], max_norm=1.0
            )
            optimizer.step()
            loss_val = float(total_loss.item())
        else:
            loss_val = 0.0

        elapsed = time.time() - t0

        if verbose:
            print(
                f"| scheme={best_rollout['scheme_type']:12s} "
                f"| R_inv={mean_r_inv:.3f} "
                f"| F1={best_rollout['f1']:.3f} "
                f"| steps={best_rollout['steps']:2d} "
                f"| loss={loss_val:.4f} "
                f"| {elapsed:.1f}s"
            )

        # ── Log ────────────────────────────────────────────────────────
        episode_results.append(best_rollout)
        _append_csv(LOG_CSV, ep, {**best_rollout, "r_inv": mean_r_inv})

        curves["episodes"].append(ep)
        curves["r_inv"].append(round(mean_r_inv, 4))
        curves["f1"].append(round(best_rollout["f1"], 4))
        curves["scheme_types"].append(best_rollout["scheme_type"])

        # ── Criminal Codex update ──────────────────────────────────────
        #    Every K=5 episodes, call criminal.adapt() to evolve the criminal
        _maybe_update_criminal(criminal_designer, ep, episode_results)

        # ── Checkpoint ─────────────────────────────────────────────────
        if use_model and model is not None and ep % CKPT_EVERY == 0:
            _save_checkpoint(model, tokenizer, ep)

    # ── Save reward curve ──────────────────────────────────────────────
    _save_curves(CURVES_PATH, curves)
    print(f"\n[4] Training complete.")
    print(f"  Reward curve saved to {CURVES_PATH}")
    print(f"  Per-scheme F1 log  saved to {LOG_CSV}")
    if use_model:
        print(f"  Checkpoints in {OUT_DIR}")

    # Summary stats
    r_invs = curves["r_inv"]
    if r_invs:
        print(f"\n  R_investigator:  first={r_invs[0]:.3f}  last={r_invs[-1]:.3f}  "
              f"max={max(r_invs):.3f}  mean={sum(r_invs)/len(r_invs):.3f}")

    return curves


# ---------------------------------------------------------------------------
# Dry-run mode (no GPU — validates the rollout pipeline)
# ---------------------------------------------------------------------------

def dry_run(num_episodes: int = 3) -> None:
    """
    Validate the full training pipeline without a GPU.
    Runs heuristic rollouts to prove env / reward / CSV / JSON plumbing works.
    """
    print("=" * 70)
    print("HEIST GRPO — Dry Run (heuristic rollouts, no GPU)")
    print("=" * 70)
    curves = train(
        num_episodes=num_episodes,
        use_trl_trainer=False,
        use_model=False,
        verbose=True,
    )
    print("\n[DRY RUN] Pipeline verified:")
    print(f"  Episodes run: {len(curves['episodes'])}")
    print(f"  R_inv values: {curves['r_inv']}")
    print(f"  Scheme types: {curves['scheme_types']}")

    # Verify output files
    assert os.path.exists(CURVES_PATH), f"Missing {CURVES_PATH}"
    assert os.path.exists(LOG_CSV),     f"Missing {LOG_CSV}"
    with open(CURVES_PATH, encoding="utf-8") as f:
        saved = json.load(f)
    assert len(saved["episodes"]) == num_episodes
    print(f"  training_curves.json: OK ({len(saved['episodes'])} entries)")
    print(f"  training_log.csv: OK")
    print("\nDry run PASSED — ready for Colab T4 training.")


