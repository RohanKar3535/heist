def train(
    num_episodes: int = NUM_EPISODES,
    use_trl_trainer: bool = True,   # False = lightweight manual loop (for CI)
    use_model: bool = True,          # False = heuristic rollouts (for CI without GPU)
    verbose: bool = True,
):
    """
    Full GRPO training loop.

    use_trl_trainer=True  → uses TRL GRPOConfig + standard update path
    use_trl_trainer=False → uses our manual grpo_loss() (same math, no TRL dep)
    use_model=False       → heuristic rollouts (testing/CI, no GPU required)
    """
    print("=" * 70)
    print("HEIST GRPO Training — Qwen2.5-1.5B (4-bit Unsloth)")
    print("=" * 70)
    print(f"  Episodes:      {num_episodes}")
    print(f"  GRPO G:        {GRPO_G} rollouts/prompt")
    print(f"  LR:            {LR}")
    print(f"  Codex update:  every {CODEX_UPDATE_K} episodes")
    print(f"  Checkpoint:    every {CKPT_EVERY} episodes -> {OUT_DIR}")
    if RESUME_FROM and os.path.exists(RESUME_FROM):
        print(f"  Resume from:   {RESUME_FROM}  (start_ep={START_EPISODE})")
    print()

    # ── Load model ─────────────────────────────────────────────────────
    model = tokenizer = optimizer = None

    if use_model:
        FastLanguageModel, torch, AutoTokenizer = _import_ml()

        if RESUME_FROM and os.path.exists(RESUME_FROM):
            # Resume from a saved checkpoint — LoRA adapters already merged into the
            # checkpoint, so we load directly without calling get_peft_model again.
            print(f"[1] Resuming from checkpoint: {RESUME_FROM}")
            model, tokenizer = FastLanguageModel.from_pretrained(
                model_name=RESUME_FROM,
                max_seq_length=MAX_SEQ_LEN,
                load_in_4bit=True,
                dtype=None,
            )
        else:
            print("[1] Loading Qwen2.5-1.5B with Unsloth 4-bit quantization...")
            model, tokenizer = FastLanguageModel.from_pretrained(
                model_name=MODEL_NAME,
                max_seq_length=MAX_SEQ_LEN,
                load_in_4bit=True,  # 4-bit for T4 (fits in 16GB VRAM)
                dtype=None,         # auto-detect bfloat16/float16
            )

            # LoRA adapters (PEFT — only fine-tune adapter layers)
            model = FastLanguageModel.get_peft_model(
                model,
                r=16,                       # LoRA rank
                target_modules=["q_proj", "k_proj", "v_proj", "o_proj",
                                 "gate_proj", "up_proj", "down_proj"],
                lora_alpha=16,
                lora_dropout=0,
                bias="none",
                use_gradient_checkpointing="unsloth",  # saves VRAM on T4
                random_state=42,
            )

        model.train()
        # Clear max_length from generation config so max_new_tokens doesn't
        # trigger the "Both max_new_tokens and max_length set" warning flood.
        if hasattr(model, "generation_config"):
            model.generation_config.max_length = None

        import torch
        optimizer = torch.optim.AdamW(
            [p for p in model.parameters() if p.requires_grad],
            lr=LR,
        )
        print(f"  Model loaded. Trainable params: {sum(p.numel() for p in model.parameters() if p.requires_grad):,}")
    else:
        print("[1] Heuristic mode (no GPU) — model=None")

    # ── Setup env + agents ─────────────────────────────────────────────
    print("[2] Initialising HEIST environment...")
    env = HeistEnvironment()

    # Criminal Designer (optional — skipped if not importable)
    criminal_designer = None
    try:
        from agents.criminal import CriminalDesigner
        criminal_designer = CriminalDesigner(temperature=1.0, seed=42)
        print("  CriminalDesigner loaded.")
    except Exception as e:
        print(f"  CriminalDesigner not available: {e}")

    # Red Queen Curriculum (ELO-driven scheme selection)
    curriculum = RedQueenCurriculum()
    print("  RedQueenCurriculum loaded (ELO-driven scheme selection).")

    # Compliance Expert (preference drift — wired into run_rollout compliance score)
    expert = None
    try:
        sys.path.insert(0, os.path.join(_ROOT, "agents"))
        from agents.expert import ComplianceExpert
        expert = ComplianceExpert()
        print("  ComplianceExpert loaded.")
    except Exception as e:
        print(f"  ComplianceExpert not available: {e}")

    # ── Init logging ───────────────────────────────────────────────────
    os.makedirs(OUT_DIR, exist_ok=True)
    _init_csv(LOG_CSV)
    curves: Dict[str, Any] = {
        "episodes": [],
        "r_inv":    [],
        "f1":       [],
        "scheme_types": [],
        "config": {
            "model": MODEL_NAME,
            "num_episodes": num_episodes,
            "grpo_g": GRPO_G,
            "lr": LR,
        }
    }

    # ── Training loop ──────────────────────────────────────────────────
    start_ep = START_EPISODE
    end_ep   = start_ep + num_episodes - 1
    print(f"\n[3] Training for {num_episodes} episodes (ep {start_ep} → {end_ep})...\n")
    episode_results: List[Dict] = []

    for ep in range(start_ep, end_ep + 1):
        t0 = time.time()
        seed = ep * 17  # deterministic but varied seeds

        if verbose:
            print(f"Episode {ep:3d}/{num_episodes}", end="  ")

        # Red Queen: pick scheme type for this episode via ELO-weighted curriculum.
        # Previously env.reset() chose randomly → trade_based could dominate by luck.
        # Now the curriculum drives selection: high-ELO (hard) schemes sampled more.
        target_scheme = curriculum.select_scheme_type(ep)

        # Collect G rollouts (GRPO requires multiple samples per prompt)
        rollouts = []
        for g in range(GRPO_G):
            # Spread temperature across rollouts: 0.5 (conservative) → 0.9 (exploratory).
            # Capped at 0.9 (was 1.1) — values above ~1.0 produce near-zero-probability
            # completions that cause log(~0) → NaN loss, killing the gradient update.
            rollout_temp = 0.5 + (g / max(GRPO_G - 1, 1)) * 0.4
            # Don't pass expert to rollouts — ComplianceExpert burns ~1400 Groq tokens
            # per call × 6 rollouts × 50 episodes = 420k tokens, exhausting the 500k/day
            # limit by ep35. Expert compliance is kept for the best-rollout log only.
            rollout = run_rollout(
                env=env,
                model=model,
                tokenizer=tokenizer,
                seed=seed + g,
                use_model=use_model,
                verbose=False,
                expert=None,
                episode_num=ep,
                temperature=rollout_temp,
                preferred_scheme_type=target_scheme,
            )
            rollouts.append(rollout)

        # Use best rollout for logging (mean r_inv across group)
        mean_r_inv = float(np.mean([r["r_inv"] for r in rollouts]))
        best_rollout = max(rollouts, key=lambda x: x["r_inv"])

        # ── GRPO update ────────────────────────────────────────────────
        if use_model and model is not None and optimizer is not None:
            # GRPO: normalize across the G rollouts by their terminal R_inv.
            # Each rollout is treated as one group member — advantage computed
            # from final episode reward so intermediate steps aren't penalized.
            import torch
            # Terminal reward signal for GRPO advantage computation.
            # - SAR filed (compliance > 0): use rewards[-1] = env_step_r + r_inv,
            #   which includes the compliance × 10 bonus. Range ~1.0–5.0, std ~1.0.
            # - Timeout (no SAR filed, compliance == 0): rewards[-1] is just the
            #   last wandering step's env reward + low r_inv — semantically ambiguous.
            #   Replace with a fixed -0.5 penalty so GRPO learns timeout = bad,
            #   not "a slightly different kind of good rollout".
            rollout_rewards = [
                (r["rewards"][-1] if r["rewards"] else r["r_inv"])
                if r["compliance"] > 0
                else -0.5
                for r in rollouts
            ]
            rewards_t = torch.tensor(rollout_rewards, dtype=torch.float32)
            mean_r = rewards_t.mean()
            std_r  = rewards_t.std() + 1e-8
            advantages = ((rewards_t - mean_r) / std_r).tolist()

            optimizer.zero_grad()
            # Multi-step GRPO: 5 key steps per rollout (first, 3 intermediates, last)
            # Previously only trained on last step — now 5x more gradient signal per episode,
            # covering early-phase decisions + the critical SARFiling moment.
            all_prompts: List[str] = []
            all_completions: List[str] = []
            all_advantages: List[float] = []
            for rollout_i, adv_i in zip(rollouts, advantages):
                key_steps = _sample_key_steps(rollout_i, n_train=5)
                for si in key_steps:
                    all_prompts.append(rollout_i["prompts"][si])
                    all_completions.append(rollout_i["completions"][si])
                    all_advantages.append(float(adv_i))
            total_loss = grpo_loss(
                model, tokenizer,
                all_prompts,
                all_completions,
                all_advantages,
            )
            # Skip update if loss is NaN/Inf — high-temperature rollouts can produce
            # near-zero-probability completions causing log(~0) → -inf → NaN.
            # Propagating NaN gradients corrupts model weights silently.
            if torch.isnan(total_loss) or torch.isinf(total_loss):
                if verbose:
                    print(f"  [WARN] NaN/Inf loss — skipping gradient update this episode")
                loss_val = float("nan")
            else:
                total_loss.backward()
                torch.nn.utils.clip_grad_norm_(
                    [p for p in model.parameters() if p.requires_grad], max_norm=0.5
                )
                optimizer.step()
                loss_val = float(total_loss.item())
        else:
            loss_val = 0.0

        elapsed = time.time() - t0

        if verbose:
            entropy_str = f"| H(a)={best_rollout.get('action_entropy', 0):.2f} "
            print(
                f"| scheme={best_rollout['scheme_type']:12s} "
                f"| R_inv={mean_r_inv:.3f} "
                f"| F1={best_rollout['f1']:.3f} "
                f"| steps={best_rollout['steps']:2d} "
                f"| loss={loss_val:.4f} "
                f"{entropy_str}"
                f"| {elapsed:.1f}s"
            )

        # ── Update Red Queen curriculum with episode result ────────────
        curriculum.update({
            "scheme_type":    best_rollout["scheme_type"],
            "f1":             best_rollout["f1"],
            "morph_succeeded": False,
            "novel_evaded":    False,
        })

        # ── Log ────────────────────────────────────────────────────────
        episode_results.append(best_rollout)
        _append_csv(LOG_CSV, ep, {**best_rollout, "r_inv": mean_r_inv})

        curves["episodes"].append(ep)
        curves["r_inv"].append(round(mean_r_inv, 4))
        curves["f1"].append(round(best_rollout["f1"], 4))
        curves["scheme_types"].append(best_rollout["scheme_type"])

        # ── Dynamic exploration: bump temperature when performance drops ──
        # Only escalate when >10% below rolling average (was 5% — too sensitive,
        # fired 22% of episodes and kept temperature pinned at 1.5 cap).
        # Cool faster: 0.93/ep instead of 0.97/ep (recovers in ~8 eps not 17).
        if criminal_designer is not None and ep > 5:
            recent_rinvs = curves["r_inv"][-5:]
            rolling_avg = float(np.mean(recent_rinvs)) if recent_rinvs else 0.0
            if rolling_avg > 0 and mean_r_inv < rolling_avg * 0.90:
                criminal_designer.temperature = min(1.5, criminal_designer.temperature * 1.2)
                if verbose:
                    print(f"  [Explore] Perf drop detected (R={mean_r_inv:.3f} < avg={rolling_avg:.3f}) — temperature={criminal_designer.temperature:.2f}")
            else:
                criminal_designer.temperature = max(1.0, criminal_designer.temperature * 0.93)

        # ── Criminal Codex update ──────────────────────────────────────
        #    Every K=5 episodes, call criminal.adapt() to evolve the criminal
        _maybe_update_criminal(criminal_designer, ep, episode_results)

        # ── Checkpoint ─────────────────────────────────────────────────
        if use_model and model is not None and ep % CKPT_EVERY == 0:
            _save_checkpoint(model, tokenizer, ep)

    # ── Save reward curve ──────────────────────────────────────────────
    _save_curves(CURVES_PATH, curves)
    print(f"\n[4] Training complete.")
    print(f"  Reward curve saved to {CURVES_PATH}")
    print(f"  Per-scheme F1 log  saved to {LOG_CSV}")
    if use_model:
        print(f"  Checkpoints in {OUT_DIR}")

    # ── Interpretability summary ───────────────────────────────────────
    entropies = [r.get("action_entropy", 0.0) for r in episode_results if r.get("action_entropy") is not None]
    if entropies:
        first5  = float(np.mean(entropies[:5]))
        last5   = float(np.mean(entropies[-5:]))
        print(f"\n  [Interpretability] Action entropy:")
        print(f"    First 5 eps avg:  H={first5:.3f}  (higher = more random)")
        print(f"    Last  5 eps avg:  H={last5:.3f}  (lower  = more decisive)")
        if last5 < first5 - 0.05:
            print(f"    ✓ Agent became MORE decisive over training (ΔH={last5-first5:.3f})")
        else:
            print(f"    ~ No clear decisiveness improvement (ΔH={last5-first5:.3f})")

    # ── Final ELO table ────────────────────────────────────────────────
    print(f"\n  [RedQueen] Final ELO standings:")
    print(curriculum.scheme_elo_table())

    # ── Zero-Day synthesis ─────────────────────────────────────────────
    # Synthesize a coordinated attack from the 3 hardest schemes (by ELO).
    # This scheme is engineered from the investigator's own failure modes.
    if criminal_designer is not None:
        top3 = curriculum.top_schemes_by_elo(n=3)
        print(f"\n  [ZeroDay] Synthesizing from top-ELO schemes: {top3}")
        zd_fn, zd_info = criminal_designer.synthesize_zero_day(top3, episode_number=end_ep)
        if zd_info.get("validated"):
            print(f"  [ZeroDay] ✓ Validated. Structural novelty={zd_info['novelty_bonus']:.3f}")
            # Run 2 heuristic rollouts against the Zero-Day to measure baseline detection
            print(f"  [ZeroDay] Running heuristic detection eval...")
            zd_f1_scores = []
            for zd_seed in [7777, 8888]:
                zd_rollout = run_rollout(
                    env=env, model=None, tokenizer=None,
                    seed=zd_seed, use_model=False, verbose=False,
                    expert=None, episode_num=end_ep,
                )
                zd_f1_scores.append(zd_rollout["f1"])
            zd_f1 = float(np.mean(zd_f1_scores))

            # Compare against mean F1 on known schemes
            known_f1 = float(np.mean(curves["f1"][-10:])) if curves["f1"] else 0.0
            print(f"  [ZeroDay] Heuristic F1 on Zero-Day: {zd_f1:.3f}")
            print(f"  [ZeroDay] Known-scheme F1 (last 10 ep): {known_f1:.3f}")
            gap = known_f1 - zd_f1
            if gap > 0.05:
                print(f"  [ZeroDay] ✓ F1 gap = {gap:.3f} — Zero-Day harder than known schemes!")
            else:
                print(f"  [ZeroDay] ~ F1 gap = {gap:.3f} — Zero-Day not significantly harder")
        else:
            print(f"  [ZeroDay] Generation failed: {zd_info.get('validation_error', 'unknown')}")

    # Summary stats
    r_invs = curves["r_inv"]
    if r_invs:
        print(f"\n  R_investigator:  first={r_invs[0]:.3f}  last={r_invs[-1]:.3f}  "
              f"max={max(r_invs):.3f}  mean={sum(r_invs)/len(r_invs):.3f}")

    return curves


