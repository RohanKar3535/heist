# ---------------------------------------------------------------------------

if __name__ == "__main__":
    import argparse
    parser = argparse.ArgumentParser(description="HEIST GRPO Training")
    parser.add_argument("--dry-run",      action="store_true", help="Run without GPU (heuristic rollouts)")
    parser.add_argument("--episodes",     type=int, default=NUM_EPISODES)
    parser.add_argument("--no-trl",       action="store_true", help="Use manual GRPO loss instead of TRL")
    parser.add_argument("--quiet",        action="store_true")
    args = parser.parse_args()

    if args.dry_run:
        dry_run(num_episodes=args.episodes)
    else:
        train(
            num_episodes=args.episodes,
            use_trl_trainer=not args.no_trl,
            use_model=True,
            verbose=not args.quiet,
        )
